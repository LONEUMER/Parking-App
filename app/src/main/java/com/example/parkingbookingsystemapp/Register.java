package com.example.parkingbookingsystemapp;

public class Register {
    String Name;
    Integer Vechical;
    Long Phone;
    String Slot;
    public Register() {
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public Integer getVechical() {
        return Vechical;
    }

    public void setVechical(Integer vechical) {
        Vechical = vechical;
    }

    public Long getPhone() {
        return Phone;
    }

    public void setPhone(Long phone) {
        Phone = phone;
    }

    public String getSlot() {
        return Slot;
    }

    public void setSlot(String slot) {
        Slot = slot;
    }
}
